package com.visiabletech.smilmobileapp.Pogo;

public class AssignmentFileModel {
    private String file_url;
    private String asst_id;
    private String topic_text;

    public String getFile_url() {
        return file_url;
    }

    public void setFile_url(String file_url) {
        this.file_url = file_url;
    }

    public String getAsst_id() {
        return asst_id;
    }

    public void setAsst_id(String asst_id) {
        this.asst_id = asst_id;
    }

    public String getTopic_text() {
        return topic_text;
    }

    public void setTopic_text(String topic_text) {
        this.topic_text = topic_text;
    }

}

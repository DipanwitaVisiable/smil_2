package com.visiabletech.smilmobileapp.Pogo;

public class SubjectListInfo {
    private String id;
    private String subject_name;
    private String class_or_year;
    private String created_date;
    private String modified_date;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSubject_name() {
        return subject_name;
    }

    public void setSubject_name(String subject_name) {
        this.subject_name = subject_name;
    }

    public String getClass_or_year() {
        return class_or_year;
    }

    public void setClass_or_year(String class_or_year) {
        this.class_or_year = class_or_year;
    }

    public String getCreated_date() {
        return created_date;
    }

    public void setCreated_date(String created_date) {
        this.created_date = created_date;
    }

    public String getModified_date() {
        return modified_date;
    }

    public void setModified_date(String modified_date) {
        this.modified_date = modified_date;
    }

}

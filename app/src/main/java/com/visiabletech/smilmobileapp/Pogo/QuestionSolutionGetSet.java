package com.visiabletech.smilmobileapp.Pogo;

public class QuestionSolutionGetSet {

    private String question_id;
    private String question;
    private String solution;
    private String student_answer_id;

    public String getQuestion_id() {
        return question_id;
    }

    public void setQuestion_id(String question_id) {
        this.question_id = question_id;
    }

    public String getQuestion() {
        return question;
    }

    public void setQuestion(String question) {
        this.question = question;
    }

    public String getSolution() {
        return solution;
    }

    public void setSolution(String solution) {
        this.solution = solution;
    }


    public String getStudent_answer_id() {
        return student_answer_id;
    }

    public void setStudent_answer_id(String student_answer_id) {
        this.student_answer_id = student_answer_id;
    }

}
